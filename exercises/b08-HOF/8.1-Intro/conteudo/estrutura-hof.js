// Usando como base o exemplo anterior, passamos como segundo parâmetro uma outra função, que será executada quando chamarmos ela dentro da função 'repeat'
const repeat = (number, action) => {
  for (let count = 0; count <= number; count += 1) {
    action(count);
  }
};

repeat(3, (number) => {
  if (number % 2 === 0) {
    console.log(number, 'is even');
  }
});

